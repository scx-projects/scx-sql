package dev.scx.sql.handler;

import dev.scx.reflect.TypeInfo;

/// SQL 读写抽象
///
/// ### 这段注释要记录的, 不只是一个接口为何长这样
///
/// 这不是一个随手写出来的 bind/read 小接口,
/// 而是一次长时间推演之后的收敛结果.
///
/// 它真正要记录的, 不是某个技巧为何没成功, 而是一次更根本的判断:
///
/// > 当前问题的症结, 不是 "还差一个更强的中间层",
/// > 而是继续追求 "统一中心" 本身, 很可能就是错误目标.
///
/// 如果以后再次产生下面这类念头:
///
/// >  是不是只要再找到一个更大的统一抽象, object / node / json / sql 就都能围绕同一个中心运行?
///
/// 请先完整看完这段注释.
///
///
/// ### 最初想追求的路线
///
/// 一开始希望整个体系存在一个统一中间表示, 使得:
///
/// - object -> node -> json
/// - object -> node -> sql
///
/// 都走同一条主链.
///
/// 这个想法之所以极具诱惑力, 是因为它看起来最纯:
///
/// 1. 整个体系只有一个中心
/// 2. object 映射逻辑只需要围绕一个中心展开
/// 3. JSON 落地和数据库落地, 看起来都只是 "对象投影到另一种存储介质"
/// 4. 一旦这条路成立, node 看起来既能服务文档序列化, 也能服务数据库落地
///
/// 换句话说, 最初想追求的是:
///
/// > 让 node 成为 object / json / sql 之间的统一中心.
///
///
/// ### 后来确认的根本矛盾
///
/// 继续推演后发现, 最开始其实是在同时追求下面三件事:
///
/// 1. scx-node 继续保持现在这种简单、闭合、纯净的小模型
/// 2. JSON 与 SQL 共用同一条统一中心主链
/// 3. 整个体系里不要出现第二个中心模型
///
/// 最终确认:
///
/// > 这三件事不能同时成立.
///
/// 如果坚持 2, 往往就会伤到 1;
/// 如果坚持 1, 通常就必须放弃 2, 或者接受某种额外中心;
/// 如果再拒绝 3, 则很容易重新回到双中心张力问题.
///
/// 也就是说:
///
/// > 当前问题并不是 "还差一点设计技巧";
/// > 而是最初追求的几个目标本身互相冲突.
///
///
/// ### 为什么这条路后来被放弃
///
/// 因为继续推演后发现, 当前 scx-node 并不是 "对象世界的无损通用 IR",
/// 它更准确的定位其实是:
///
/// > 对象的数据树投影.
///
/// 更具体地说, scx-node 适合承载的是:
///
/// - 树形
/// - 无共享引用
/// - 无身份语义
/// - 偏 value-centric
/// - 偏文档 / 配置 / 序列化
///
/// 的那部分数据世界.
///
/// 而数据库 / JDBC 面对的并不是同一件事.
///
/// 数据库这边真正遇到的是:
///
/// - typed bind
/// - typed read
/// - byte[] / 二进制
/// - 日期 / 时间 / 时间戳
/// - typed null
/// - 列语义 / JDBC 类型语义
/// - 名称与索引并存
/// - operation-oriented 的读写事实
///
/// 这些问题说明:
///
/// > SQL 并不是 "树形值投影的另一个出口",
/// > 而是另一套外部模型.
///
///
/// ### 一个必须明确的事实
///
/// 需要特别强调的是:
///
/// > object -> node -> object 这条路径本身也不是严格无损的 (scx-object-x).
///
/// 过去之所以很多时候还能成立, 不是因为 node 足够强,
/// 而是因为反向时通常还保留着目标类型元数据,
/// 这些元数据会替 node 补回一部分语义.
///
/// 例如:
///
/// - 某些日期/时间值虽然被投影成 StringNode, 但反向时仍然可以依赖目标字段类型 parse 回去
/// - 某些特殊值虽然经过了投影, 但因为反向字段类型仍然明确, 还能被重新解释
///
/// 这说明:
///
/// > node 从来就不是对象世界的无损通用中间表示,
/// > 它只是对象的数据树投影.
///
/// SQL 场景并没有创造一个全新的问题.
/// 它只是因为在 node -> sql 这条路径上,
/// 丢失的语义没有 "目标对象字段类型" 来天然补回,
/// 所以把 node 原本就存在的边界更早、更明显地暴露了出来.
///
/// 换句话说:
///
/// > SQL 不是偏离了 node 的主线,
/// > 而是更诚实地暴露了 node 的原有边界.
///
///
/// ### 真正暴露问题的几个典型信号
///
/// #### 1. byte[]
///
/// 在当前 scx-node 中, byte[] 并没有天然、直接、无损且语义自明的表达.
///
/// 理论上当然可以做各种投影:
///
/// - base64 String
/// - int 数组
/// - 特殊 ObjectNode 约定
///
/// 但这些都不是 "天然值表示", 而是 "约定后的投影".
///
/// 这意味着:
///
/// > 一旦 object 先被投影成 node, 然后再去绑定 SQL,
/// > "原来它是二进制" 这层语义就不再自然存在.
///
///
/// #### 2. 日期 / 时间
///
/// 日期类问题更能说明本质.
///
/// 例如某个时间值被转成 StringNode 后:
///
/// - 在 object <- node 这条路上, 因为目标字段类型仍然存在,
///   还可以根据字段类型 parse 回去
/// - 但在 node -> sql 这条路上, 如果只剩一个 node 值,
///   那么它到底应该绑定成 DATE、TIME、TIMESTAMP, 还是其他形式,
///   这层信息并不天然保留在 node 中
///
/// 也就是说:
///
/// > object -> node -> object 之所以很多时候还能成立,
/// > 不是因为 node 足够强,
/// > 而是因为反向时目标类型元数据替 node 补回了语义.
///
/// SQL 路线只是更早、更诚实地暴露了这个问题.
///
///
/// #### 3. typed null / missing / skipped value
///
/// 文档型树模型里的 null, 与数据库绑定里的 null, 并不是同一件事.
///
/// 数据库里实际会遇到:
///
/// - 值存在且为 null
/// - 值缺失
/// - 参数被跳过
/// - 需要 default value
/// - 需要带 JDBC/SQL 类型语义的 null
///
/// 这些语义在 node 里并不会天然保留.
///
/// 这进一步说明:
///
/// > SQL/JDBC 关心的不是一个抽象的 "空值",
/// > 而是具有上下文和类型语义的 operation.
///
///
/// #### 4. 名称 / 索引 / 列语义
///
/// 树模型天然偏 name/path.
/// JDBC 则天然存在 index / column label / column type / dialect 这些事实.
///
/// 一旦试图让同一个中心同时服务这两边,
/// 很快就会被迫同时处理:
///
/// - 按 name 读
/// - 按 index 读
/// - 列类型 hint
/// - target type hint
/// - 其他上下文信息
///
/// 这不是一个偶然细节,
/// 而是在说明:
///
/// > 两边面对的不是同一种交互事实.
///
///
/// ### 因此最终确认的一件事
///
/// scx-node 不是数据库绑定的统一 IR.
///
/// 更准确地说:
///
/// - scx-node 是树形数据投影模型
/// - SQL/JDBC 是 typed operation 模型
///
/// 两者不是同一个层次的东西.
///
/// 如果为了统一 SQL 而继续扩张 scx-node,
/// 最终发生的不会是 "更纯" , 而是:
///
/// - node 的边界被打破
/// - node 的简单性下降
/// - node 的闭合性被侵蚀
/// - node 从一个小而硬的树模型, 变成一个越来越大的富类型值模型
///
/// 这与当前 scx-node 最珍贵的设计目标相冲突.
///
///
/// ### 还确认了另一件事
///
/// 真正错误的目标, 并不是 "还没有找到一个更大的统一值域",
/// 而是:
///
/// > 在当前体系下继续追求 "统一中心" 本身, 很可能就是错误目标.
///
/// 因为一旦继续沿这个方向前进, 常见结果无非是:
///
/// - 继续扩大 scx-node 的值域
/// - 将 node 拆成 core / extension
/// - 再造一个更大的统一值域
/// - 再造一个更大的统一协议域
/// - 为 SQL 再造一个数据库版 Node / DbValue
///
/// 这些方案表面上都在追求更纯,
/// 但只要它们不能真正成为整个体系唯一、稳定、自然的中心,
/// 最终都会回到同一个问题:
///
/// > 新中心没有真正解决问题, 只是平移了问题,
/// > 或者把代价转移到了原本不该承受代价的边界上.
///
/// 因此最终放弃继续追求 "统一中心" 作为当前问题的主要目标.
///
///
/// ### 曾经认真考虑过, 但最终放弃的几条路线
///
/// #### 方案一: 直接增强 scx-node, 让 object -> node -> sql 成立
///
/// 这是最诱人的路线, 也是最先考虑的路线.
///
/// 典型做法包括:
///
/// - 给 node 增加 BinaryNode
/// - 给 node 增加日期/时间类节点
/// - 给 node 增加 richer scalar
/// - 继续扩大 node 的值域
///
/// 这条路的问题不是做不到, 而是代价非常明确:
///
/// 1. scx-node 不再是原来那个简单、闭合、偏树形的模型
///
/// 2. 一旦接受 richer scalar, 后面自然会继续出现:
///    - BinaryNode
///    - DateNode / TimeNode / TimestampNode
///    - UUIDNode
///    - typed null
///    - 其他特殊值节点
///
/// 3. node 的定位会发生路线级变化
///
/// 4. node 将不再是一个小而硬的树投影模型,
///    而会逐步滑向一个越来越大的通用值系统
///
/// 换句话说:
///
/// > 这不是小修小补,
/// > 而是把 scx-node 改造成另一个产品.
///
/// 最终放弃.
///
///
/// #### 方案二: 将 node 拆分为 CoreNode / ExtensionNode
///
///                  Node
///              ______|______
///             /             \
///            /               \
///    CoreNode(原 Node)     ExtensionNode(允许拓展)
///                              |
///                         asCoreNode()
///                              |
///                              v
///                          CoreNode (可回到原体系)
///
/// 这个方案本质上是在尝试:
///
/// - 保留 node 原有核心 -> CoreNode
/// - 同时为 richer value / database value 开扩展口 -> ExtensionNode
/// - ExtensionNode 自身提供能够转换回 CoreNode 的方法
///
/// 它的问题在于:
///
/// 1. 看似没改 node, 实际上破坏了 node 原本封闭、可穷举的世界
///
/// 2. ObjectNode / ArrayNode 里存什么?
///    - 存 Node     -> ExtensionNode 能进树, 闭合性被打破
///    - 存 CoreNode -> ExtensionNode 进树前必须先降级
///
/// 3. deepCopy 属于谁?
///    - 属于 Node     -> ExtensionNode 未必天然拥有一致复制语义
///    - 属于 CoreNode -> ExtensionNode 进树前必须先降级
///
/// 4. equals / toString 按谁的语义算?
///    - 按 ExtensionNode 原语义?
///    - 按 asCoreNode() 之后的投影语义?
///
/// 5. 长期看, 这会把一个小闭合集变成 "可以被不断加层" 的系统
///
/// 当然方案二还有一些变体:
///
/// 例如:
///
/// - 只允许拓展值节点 ExtensionValueNode
/// - 不可继承的实例化 ExtensionNode
///
/// 但这些变体本质上都存在同样的问题:
///
/// > 不是把问题解决了,
/// > 而是把闭合性问题从 node 内部推迟、转写、重新命名.
///
/// 最终放弃.
///
///
/// #### 方案三: 再造一个比 node 更大的统一值域
///
/// 这个方案理论上最完整:
///
/// - object 先转到一个 richer value domain
/// - node 是它的一个投影
/// - sql 是它的另一个投影
///
/// 这条路的诱惑力在于:
///
/// 1. 只要中心值域足够大, 看起来 node 与 sql 就都只是它的外部投影
///
/// 2. object 映射逻辑似乎终于可以围绕一个真正 "更强的中心" 展开
///
/// 3. 很多眼前在 node / sql 边界上暴露的问题, 看起来都只是因为当前值域还不够大
///
/// 4. 它看起来比直接扩 node 更干净:
///    - 不直接污染 node
///    - 只是在 node 之上再造一个更大的总值域
///
/// 但继续推演后发现:
///
/// 1. 如果这个新值域不取代 node 成为整个体系唯一中心,
///    那它只是第二个中心, 本质上重复了之前的问题
///
/// 2. 如果它真的取代 node 成为唯一中心, 那就意味着:
///    - 现有围绕 scx-node 的体系地位都要重排
///    - 需要重做一整套以新值域为中心的 object 映射
///    - node 将退化为它的一个投影层, 不再是原来那个小而硬的树模型
///
/// 3. 这个新值域极有可能继续吸收 richer scalar:
///    - binary
///    - date / time / timestamp
///    - uuid
///    - typed null
///    - 其他特殊值
///
/// 4. 一旦它开始持续吸收这些语义,
///    它就不再是一个自然、稳定、闭合的小中心,
///    而是一个不断膨胀的新总值域
///
/// 5. 从长期看, 它很容易退化为方案二,
///    或者至少产生与方案二同类的问题:
///    - 闭合性下降
///    - 中心边界变软
///    - 旧问题只是被平移
///
/// 也就是说:
///
/// > 方案三不是在消灭问题,
/// > 而是在尝试用一个更大的值域吞掉问题.
/// > 只要吞不干净, 它就会变成第二中心;
/// > 如果真吞干净, 那整个体系就已经换轴了.
///
/// 最终放弃.
///
///
/// #### 方案四: 不再统一值对象, 而是统一读写协议 / binding 主轴
///
/// 在放弃 "统一值域" 之后,
/// 还会自然产生另一个极具诱惑力的变体 (scx-binding):
///
/// > 既然统一值对象走不通,
/// > 那是否可以不再统一 "值域" ,
/// > 而改为统一 "读写协议" 或 "binding 主轴" ?
///
/// 也就是:
///
/// - 不再让 node 成为总中心
/// - 不再要求 sql 感知 node
/// - 另造一套更大的 BindingInput / BindingOutput 协议
/// - object 绑定逻辑围绕这套统一协议展开
/// - node / sql / 未来其他媒介都只是这套协议的适配器
///
/// 这条路之所以极具诱惑力, 是因为它表面上看起来比 方案三 更克制:
///
/// 1. 不直接扩张 scx-node, 因此 node 本身仍然可以 0 感知
///
/// 2. scx-sql 也可以继续 0 感知 node
///
/// 3. 看起来只是把重复的 "绑定动作" 提炼出来,
///    而不是再造一个新的总值对象
///
/// 4. object-x 与 sql 中 selector / mapper / handler 的结构相似性,
///    会进一步强化这种 "应该还能再抽一层" 的直觉
///
/// 5. 它尤其容易在如下场景中诱发误判:
///    - Bean / Record 的拆装流程看起来很像
///    - `TypeNodeMapper` / `TypeSQLHandler` 看起来很像
///    - selector / factory / cache 机制看起来也很像
///
/// 于是会自然产生这种念头:
///
/// >  "既然值域统一做不到,
/// > 那就至少把读取源 / 输出源 / binding 行为统一掉."
///
/// 但这里必须明确一个在初期极难察觉、但决定性的事实:
///
/// > 这条路的问题, 不是它做不到,
/// > 而是:
/// > 它越成功, 越危险.
///
/// 因为在最初阶段, 这套抽象会表现得非常自然且令人信服:
///
/// - BeanBinding / RecordBinding 可以很好抽象
/// - 对象字段遍历 / 构造 / 赋值流程高度一致
/// - read/write 结构呈现良好的对称性
/// - policy / selector / cache 机制都可以复用
///
/// 这会制造一个极强的错觉:
///
/// > 统一 binding 主轴已经接近完成.
///
/// 但关键在于:
///
/// > 它不会停在这个阶段.
///
/// 一旦它试图成为 "真正有用" 的 binding 抽象,
/// 就必然沿着如下路径继续演化:
///
/// ##### (1) 首先吸收值转换
///
/// 这一步看起来最合理, 也最容易被接受:
///
/// - int / long / boolean
/// - String
/// - enum
/// - temporal
/// - null -> target type
///
/// 到这里为止, 一切仍然看起来像是 "合理增强".
///
///
/// ##### (2) 然后开始吸收媒介语义
///
/// 一旦需要同时服务 node / sql,
/// 协议就必须开始回答:
///
/// - 按 name 读还是按 index 读
/// - typed null 如何表达
/// - missing / default / skipped value
/// - source 是否存在该字段
/// - enum 使用 name 还是 ordinal
/// - temporal 精度与映射策略
///
/// 此时 binding 已经不再只是对象装配,
/// 而开始承担 "值语义解释".
///
///
/// ##### (3) 接下来, 协议会被不同外部模型共同拉扯
///
/// - node 偏 name / tree / value
/// - jdbc 偏 index / column / typed operation
///
/// 为了同时适配:
///
/// - 一个 read 不得不同时接收 name / type / index
/// - 接口开始出现 readNullValue / readMissingValue 一类补丁方法
/// - 为了兼容更多媒介不断增加 hint / option / capability
///
/// 这些信号说明的不是 "接口还不够优雅",
/// 而是:
///
/// > 抽象本身已经不再自然,
/// > 而是在被多个异质边界共同拉扯.
///
///
/// ##### (4) 在这一阶段, binding 的角色已经发生变化
///
/// 它不再只是:
///
/// - object-side 的 binding support
///
/// 而开始承担:
///
/// - 值解释
/// - null / missing / default 语义
/// - name / index 协调
/// - 不同媒介能力差异
///
/// 也就是说:
///
/// > 它已经从一个工具层,
/// > 演化为新的协议中心.
///
///
/// ##### (5) 一旦成为中心, 它就无法闭合
///
/// 因为:
///
/// - node 仍然有自己的语义
/// - sql 仍然有自己的语义
/// - object 仍然有自己的语义
///
/// 这套 binding 并没有消除这些差异,
/// 而只是:
///
/// > 将原本的张力从 node / sql / object 三方,
/// > 平移到了 binding 层.
///
/// 随之而来的必然结果是:
///
/// - 接口不断膨胀
/// - 语义不断通过 patch 追加
/// - capability / hint 持续增加
/// - 抽象越来越依赖上下文
///
/// 最终:
///
/// > 它不会成为统一中心,
/// > 而只会成为一个无法闭合的第二中心.
///
///
/// ##### (6) 因此必须明确
///
/// 真正的问题不是:
///
/// >  "还没有找到一个足够优雅的 BindingInput / BindingOutput 接口"
///
/// 而是:
///
/// > 只要它继续承担跨媒介语义,
/// > 它就必然从 "对象绑定工具"
/// > 演化为 "新的统一中心",
/// > 并重复统一中心的所有问题.
///
///
/// ##### 因此方案四最终确认
///
/// 不论是继续抽一个 scx-binding 公共主轴,
/// 还是统一 BindingInput / BindingOutput 协议,
/// 本质上都属于同一方向:
///
/// > 不再统一值对象,
/// > 而是试图统一协议中心.
///
/// 但只要目标仍然是 "再造一个更大的统一中心" ,
/// 最终都会回到同一类问题:
///
/// > 只要它不是唯一中心, 本质上就和之前的问题没有区别;
/// > 如果它要成为唯一中心, 那就已经不是当前项目要付出的代价.
///
/// 因此最终放弃这条路线.
///
///
/// #### 方案五: 为 SQL 设计一个数据库版 Node / DbValue
///
/// 例如:
///
/// - DbValue
/// - SqlNode
/// - RowNode
///
/// 然后:
///
/// - object -> DbValue -> sql
/// - resultset -> DbValue -> object
///
/// 这个方案表面上很自然,
/// 因为它看起来为数据库侧也补出了一个中间层:
///
/// - object 不再直接面对 JDBC
/// - sql 也不再直接面对 object
/// - 数据库读写似乎终于也有了一个像 node 那样的 "中间表示"
///
/// 这条路并不是完全做不到.
/// 从工程上说, 它当然是可行的:
///
/// - 可以先把对象投影成某种 DbValue
/// - 再由 DbValue 执行参数绑定
/// - 也可以先把 ResultSet 一列或一行读成 DbValue
/// - 再由 DbValue 还原成目标对象
///
/// 但继续推演后发现, 这条路的问题不在于 "不可实现",
/// 而在于:
///
/// > 它即使可行, 也未必自然；
/// > 即使自然于局部, 也未必值得成为整个数据库侧的主轴.
///
/// 更具体地说, 它的问题在于:
///
/// 1. 如果它不是整个体系唯一、稳定、自然的中心,
///    那它本质上就只是 node 的另一个镜像,
///    只是把原来的问题换了一个名字重新出现
///
/// 2. 一旦引入它, 通常仍然需要一整套新的 object 映射链
///
///    - object -> DbValue
///    - DbValue -> object
///    - DbValue -> sql
///    - resultset -> DbValue
///
///    也就是说, 它并不会天然消除复杂度,
///    很可能只是把原本直接面对 JDBC 的问题,
///    改写成先经过一个中间值对象再处理
///
/// 3. 本质上, 它仍然是在平移问题, 而不是消灭问题
///
///    原本 SQL / JDBC 面对的那些事实:
///
///    - typed bind
///    - typed read
///    - typed null
///    - temporal / binary
///    - name / index / column type
///
///    并不会因为引入 DbValue 而自动消失.
///    这些问题最终仍然必须被回答,
///    只是从 "直接回答" 变成了 "先包装成 DbValue 再回答"
///
/// 4. 它看起来比直接扩 node 更独立,
///    但独立不等于自然.
///    它仍然需要回答:
///
///    - 为什么它值得成为中心
///    - 为什么它不是第二中心
///    - 为什么 object 映射要围绕它重组
///
/// 5. 还需要特别补充一点:
///
///    > 如果这个 DbValue / SqlNode / RowNode 根本不外泄,
///    > 只在模块内部短暂存在,
///    > 那么这层 "数值抽象" 本身就很可能是多余的.
///
///    因为这时它既不是上层稳定语义的一部分,
///    也不是整个体系真正成立的公共中心,
///    而只是:
///
///    - 把原本可以直接进行的 typed bind / typed read
///      先包装成一个 "数据库值对象"
///    - 再由这个对象转回 JDBC 操作
///
///    这时它带来的就未必是复杂度下降,
///    而可能只是增加了一层中转:
///
///    - 本来可以直接 bind 的值, 先转成 DbValue 再 bind
///    - 本来可以直接 read 的列, 先转成 DbValue 再解释
///
///    换句话说:
///
///    > 如果这层对象既不外部成立,
///    > 也不内部显著降低复杂度,
///    > 那它就很容易退化成为了模仿“统一值对象”形式,
///    > 而额外构造的一层转换逻辑.
///
/// 6. 当然, 这并不意味着内部绝不能存在此类 support object
///
///    如果它只是:
///
///    - 局部的
///    - 短程的
///    - 明确收缩边界的
///    - 纯工程辅助性的
///
///    那么它可以是可行的,
///    甚至在某些局部实现里也可能是合理的.
///
///    真正需要避免的, 不是任何内部辅助对象的存在,
///    而是:
///
///    > 将一个原本只是局部辅助的中转对象,
///    > 误提升为数据库侧新的统一中心.
///
/// 7. 因此方案五最终放弃,
///    不是因为这条路 "完全不能做",
///    而是因为:
///
///    > 作为局部工程技巧, 它可以存在;
///    > 但作为数据库侧的中心抽象, 它并不自然,
///    > 也没有充分理由值得让整个对象映射体系围绕它重组.
///
/// 最终放弃.
///
///
/// ### 从这些推演中, 最后确认的一件事
///
/// 真正稳定可共享的, 只有极少数机械性、对象侧、无世界观的内容;
/// 一旦继续往上抽成统一 read/write 协议,
/// 就会重新滑回方案四.
///
/// 也就是说:
///
/// - 反射缓存
/// - 字段/参数元数据
/// - 构造器调用
/// - get/set 之类机械动作
///
/// 这些东西也许可以作为 internal/support 工具存在.
///
/// 但只要它开始拥有:
///
/// - 统一 Input / Output
/// - 跨媒介 read/write 协议
/// - null / missing / default 语义
/// - name / index / type / hint 的组合接口
///
/// 那它就不再是一个 "薄工具层",
/// 而是在重新发明一个 protocol-centric 的统一中心.
///
/// 这也是为什么这条路越写越容易产生一种明显的不自在感:
///
/// - 接口越来越像妥协物
/// - 方法越来越像为兼容而存在
/// - 抽象没有变得更清楚, 反而开始解释自己
///
/// 这种不自在感不是错觉,
/// 而是抽象本身在报警.
///
///
/// ### 最后为什么接受现在这种形式
///
/// 最终接受一个更贴近事实的判断:
///
/// > 对数据库来说, 中心不应该是 "统一值对象",
/// > 也不应该是 "统一绑定协议",
/// > 而应该是 "typed bind / typed read".
///
/// 也就是说:
///
/// - 写入数据库时, 关注的是 "这个值如何按 SQL 语义绑定"
/// - 读取数据库时, 关注的是 "这个列如何按 JDBC/SQL 语义读取"
///
/// 因此本模块最终选择围绕:
///
/// - ParameterBinder
/// - ResultSetReader
///
/// 这类 operation-oriented 概念设计,
/// 而不是继续围绕某个中间值对象或统一 binding 协议设计.
///
/// 当然从工程上看, 将 ParameterBinder 和 ResultSetReader 合并为一个接口更合理,
/// 于是最终收敛到 [TypeSQLHandler].
///
///
/// ### 为什么这种设计看起来会和 scx-object-x 有些相似
///
/// 这是预期内现象, 不是失败.
///
/// 原因很简单:
///
/// - object -> node
/// - object -> sql parameter
/// - resultset -> object
/// - node -> object
///
/// 这些过程都会涉及对象绑定、字段筛选、字段命名、实例化、赋值等问题.
///
/// 所以结构上出现相似性是正常的.
///
/// 但这里必须特别补充:
///
/// > 结构相似, 不等于应该继续向上抽成统一协议中心.
///
/// 过去曾经出现过一个很强的误判:
///
/// - 既然 `TypeNodeMapper` / `TypeSQLHandler` 很像
/// - Bean / Record 的绑定流程也很像
/// - selector / factory / cache 也很像
/// - 那么是不是可以继续再抽一层统一 BindingInput / BindingOutput
///
/// 后来确认:
///
/// > 这种相似性, 很多时候只说明 "对象绑定算法" 会在不同边界上重复出现,
/// > 并不自动说明这些边界天然共享一个稳定的中心.
///
/// 换句话说:
///
/// - 结构相似, 可能只是因为 object-side 的问题相似
/// - 并不意味着 node-side 与 sql-side 的事实已经足够接近
/// - 更不意味着应该再造一个统一 binding protocol
///
/// 否则很容易从 "看到重复" 走到 "误以为存在统一中心", 最终再次回到方案四.
///
/// ### 这里我们需要接受 外部模型不同导致的边界适配层 "重复"
///
/// 例如:
///
/// - `TypeNodeMapper` / `TypeSQLHandler` 的结构相似
/// - `TypeNodeMapperSelector` / `TypeSQLHandlerSelectorImpl` 的算法重复
///
/// 这类重复反映的是事实差异, 属于应当接受的重复.
///
/// 但还有一些重复我们需要小心对待.
///
/// 例如:
///
/// - Bean / Record 实例化流程
/// - 字段命名策略
/// - skip 策略
/// - 字段赋值规则
/// - 某些稳定的反射绑定规则
///
/// 这类内容将来如果在多个模块中稳定、明确、反复出现, 可以再提炼公共层.
///
/// 但这里也必须非常小心:
///
/// > 公共层只能提炼机械性、对象侧、无中心野心的内容;
/// > 一旦公共层开始承担统一 read/write 协议, 就是在重新发明方案四.
///
///
/// ### 当前方案并不是因为它最完美而被选中
///
/// 当前方案不是因为它 "理论最纯" 而被选中,
/// 而是因为它在不伤 scx-node 的前提下, 最贴近数据库 / JDBC 的事实.
///
/// 也就是说, 当前取舍不是:
///
/// > 选择最完美的统一方案
///
/// 而是:
///
/// > 在保护 node 边界的条件下, 选择最稳、最诚实、最少自伤的方案.
///
///
/// ### 当前取舍
///
/// 1. scx-node 保持简单、闭合、纯
/// 2. scx-sql 零感知 scx-node
/// 3. SQL 按数据库 / JDBC 的事实独立设计
/// 4. 优先接受边界适配层的重复
/// 5. 只有当对象绑定规则出现稳定、明确、反复出现的重复时, 才考虑抽取公共层
/// 6. 即使抽取公共层, 也只抽机械性、对象侧、无统一中心野心的内容
///
///
/// ### 额外提醒 !!!
///
/// 如果以后再次产生下面这几类念头:
///
/// > 只要再让 scx-node 强一点, SQL 与 JSON 就能统一
///
/// 或者:
///
/// > 既然统一值域走不通, 那就统一 BindingInput / BindingOutput 协议
///
/// 或者:
///
/// > 这些 selector / mapper / handler / bean binding 看起来这么像, 应该还能再往上抽一层
///
/// 请先停下来确认:
///
/// 1. 现在碰到的问题, 是不是又回到了 byte[]、日期/时间、typed null、name/index 这一类 richer semantics 问题
///
/// 2. 是否又在试图让某个中心同时扮演:
///    - 小而纯的树模型
///    - JSON/SQL 的统一总值域
///    - object / node / sql 的统一总协议
///
/// 3. 是否又在为了消灭边界适配层的表面重复, 去制造一个新的更大中心.
///
/// 4. 是否又忘了:
///    - object -> node -> object 本身也不是无损路径
///    - SQL 只是更早暴露了 node 的原有边界
///    - 当前真正放弃的不是某个技巧, 而是 "再造统一中心" 这个错误目标.
///
/// 5. 是否又把 "结构相似" 误判成了 "可以继续提炼为统一中心"
///
/// 6. 是否又把 "接口越来越别扭" 误以为只是 API 还没润好, 而不是抽象本身已经在报警
///
/// 如果答案是 "是", 那么请不要继续对 scx-node 动刀, 也不要再造一个新的 Binding 总协议.
///
/// 本接口存在的意义之一, 就是明确记录这次取舍:
///
/// > 对 SQL 来说, operation-oriented 设计比 value-object-oriented 设计更贴近事实;
/// > 对整个体系来说, 保护 scx-node 的边界,
/// > 比追求一个看似最纯的统一中心更重要.
///
/// ### 关于方言 (dialect) 的额外说明
///
/// 另外, 很容易进一步产生一个念头:
///
/// > 是否应该让 TypeSQLHandler 绑定在方言上,
/// > 或由方言持有 TypeSQLHandler?
///
/// 但这在理论上是错误的.
///
/// 因为这实际上是在假定:
///
/// > “值如何绑定 / 如何读取”
/// > 属于“方言问题”.
///
/// 而这恰恰是错位.
///
/// 方言真正应该回答的是:
///
/// - SQL 如何表达
/// - 标识符如何转义
/// - 函数 / 分页 / 锁 / DDL 如何生成
/// - 某数据库在 SQL 语言层面有哪些差异与能力
///
/// 但 TypeSQLHandler 回答的不是这些.
///
/// TypeSQLHandler 真正处理的是:
///
/// - 某个值如何按 JDBC/SQL 语义执行 typed bind
/// - 某一列如何按 JDBC/SQL 语义执行 typed read
/// - temporal / binary / enum / null 等值语义如何落到 operation
///
/// 也就是说:
///
/// > dialect 面向的是数据库语言差异;
/// > TypeSQLHandler 面向的是值语义到 JDBC operation 的映射.
///
/// 这不是同一层问题.
///
/// 更直接地说:
///
/// > 方言负责“SQL 怎么说”;
/// > TypeSQLHandler 负责“值怎么过桥”.
///
/// 因此, 如果让 dialect 持有 TypeSQLHandler,
/// 本质上就等于让“负责说 SQL 的对象”
/// 去负责“解释值如何绑定与读取”.
///
/// 这会把原本属于 binding / operation 层的问题,
/// 错误地上收到方言层.
///
/// 还必须再强调一个更重要的点:
///
/// > 驱动没有提供的能力, 不应该由方言伪造出来.
///
/// 即使我们把 dialect 的能力从纯语法差异拔高到数据库所有能力差异.
/// 如何 绑定/读取 也不应该由 dialect 来做兼容.
/// 某种值能否真正绑定入库,
/// 某种列值能否真正按目标语义读取回来,
/// 首先取决于:
///
/// - 驱动是否支持
/// - 数据库是否支持
/// - JDBC 是否暴露了对应事实
/// - 当前上下文是否足以支撑这种解释
///
/// 如果这些能力本身不存在,
/// 或没有被驱动稳定提供,
/// 那么框架最多只能:
///
/// - 明确失败
/// - 显式降级
/// - 或要求调用方提供额外策略
///
/// 而不应该让 dialect 出面“补”出一个看起来可用的兼容层.
///
/// 因为一旦这么做, 本质上就变成了:
///
/// > 把“驱动没有提供的能力”
/// > 伪装成“方言兼容出来的能力”.
///
/// 这不是在描述数据库能力边界,
/// 而是在制造能力幻觉.
///
/// 也就是说:
///
/// > 方言最多描述能力差异,
/// > 不应伪造能力本身.
///
/// 否则系统最终得到的就不会是一个诚实的分层,
/// 而是:
///
/// - 数据库/驱动本来不支持
/// - 但框架表面上似乎支持
/// - 真实边界被兼容层掩盖
/// - 成功条件越来越依赖补丁、猜测与特判
///
/// 这会让“数据库真的支持”与“框架假装支持”
/// 变得越来越难以区分.
///
/// 而且一旦这么做, dialect 很快就不会再只是 dialect.
///
/// 它会被迫逐步吸收:
///
/// - Java 类型到 JDBC 类型的映射
/// - temporal / binary / enum / null 的绑定策略
/// - read / write 差异
/// - 驱动兼容细节
/// - handler selector / registry / fallback 逻辑
///
/// 到这一步, dialect 就已经不再只是 SQL 方言描述器,
/// 而开始滑向“数据库侧新的解释中心 / 补偿中心”.
///
/// 这不是职责复用,
/// 而是在重新制造一个不该出现的中心.
///
/// 因此最终确认:
///
/// > TypeSQLHandler 与 dialect 本质上应该完全独立.
///
/// dialect 负责 SQL language boundary;
/// TypeSQLHandler 负责 typed bind / typed read boundary.
///
/// 两者可以协作,
/// 但不应互相收编.
///
/// @param <V> T
/// @author scx567888
/// @version 0.0.1
public interface TypeSQLHandler<V> extends ParameterBinder<V>, ResultSetReader<V> {

    TypeInfo valueType();

    /// 当当前位置没有可用的 SQL 值时, 返回该类型的兜底值.
    ///
    /// “没有可用的 SQL 值”当前包括两种情况:
    ///
    /// - 当前列存在, 但其值为 SQL NULL
    /// - 当前列在结果集中不存在
    ///
    /// 默认返回 null.
    /// 对于基本类型处理器, 通常会覆盖此方法并返回对应的零值,
    /// 例如 `0`、`false` 等.
    default V missingValue() {
        return null;
    }

}
